export let focusOffset = '1px';
export let focusWidth = '1px';
export let focusRadius = '0px';

export const setFocusOffset = (offset) => {
  focusOffset = offset;
}

export const setFocusWidth = (width) => {
  focusWidth = width;
}

export const setFocusRadius = (radius) => {
  focusRadius = radius;
}
